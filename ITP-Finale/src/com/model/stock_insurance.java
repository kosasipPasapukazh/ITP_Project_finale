package com.model;

public class stock_insurance {
	private int product_id;
    private double total_payment;
    private double last_paid_payment;
    private String date;
    private double remain_payment;
    
     public int getProduct_id() {
		return product_id;
	}
	public void setProduct_id(int product_id) {
		this.product_id = product_id;
	}
	public double getTotal_payment() {
		return total_payment;
	}
	public void setTotal_payment(double total_payment) {
		this.total_payment = total_payment;
	}
	public double getLast_paid_payment() {
		return last_paid_payment;
	}
	public void setLast_paid_payment(double last_paid_payment) {
		this.last_paid_payment = last_paid_payment;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public double getRemain_payment() {
		return remain_payment;
	}
	public void setRemain_payment(double remain_payment) {
		this.remain_payment = remain_payment;
	}
	
	
     
     
}
